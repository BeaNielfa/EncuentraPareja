    private void btnNoGustaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNoGustaActionPerformed
        try {
            int filaseleccionada = tabla.getSelectedRow();
            if (filaseleccionada == -1){
                JOptionPane.showMessageDialog(null, "No ha seleccionado ninguna fila.");
            } else {
                
                DataOutputStream dos = new DataOutputStream(servidor.getOutputStream());
                dos.writeBoolean(true);

                dos.writeInt(6);
                
                String email = (String)tabla.getValueAt(filaseleccionada, 1);
                 
                dos.writeUTF(email);
                
                JOptionPane.showMessageDialog(null, "Su solicitud ha sido procesada");
             
                ArrayList lu = (ArrayList) Utilidades.Util.recibirObjeto(servidor);
        
        
                rellenarTabla(lu, 0);
                
            }
         } catch (IOException ex) {
             Logger.getLogger(FrmListaLikes.class.getName()).log(Level.SEVERE, null, ex);
         } catch (ClassNotFoundException ex) {
             Logger.getLogger(FrmListaLikes.class.getName()).log(Level.SEVERE, null, ex);
         }
    }//GEN-LAST:event_btnNoGustaActionPerformed

    private void btnLikeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLikeActionPerformed
        try {
            int filaseleccionada = tablaGustar.getSelectedRow();
            if (filaseleccionada == -1){
                JOptionPane.showMessageDialog(null, "No ha seleccionado ninguna fila.");
            } else {
                
                DataOutputStream dos = new DataOutputStream(servidor.getOutputStream());
                DataInputStream dis = new DataInputStream(servidor.getInputStream());
                dos.writeBoolean(true);

                dos.writeInt(7);
                
                String email = (String)tablaGustar.getValueAt(filaseleccionada, 1);
                 
                dos.writeUTF(email);
                
                int result = dis.readInt();
                if(result == 0){
                    JOptionPane.showMessageDialog(null, "Ya le gusta este Usuario");
                }else{
                    JOptionPane.showMessageDialog(null, "Su solicitud ha sido procesada");
                }
                ArrayList lu = (ArrayList) Utilidades.Util.recibirObjeto(servidor);
        
        
                rellenarTabla(lu, 0);
                
            }
         } catch (IOException ex) {
             Logger.getLogger(FrmListaLikes.class.getName()).log(Level.SEVERE, null, ex);
         } catch (ClassNotFoundException ex) {
             Logger.getLogger(FrmListaLikes.class.getName()).log(Level.SEVERE, null, ex);
         }
    }//GEN-LAST:event_btnLikeActionPerformed

   

