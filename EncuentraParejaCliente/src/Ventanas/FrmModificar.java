    public FrmModificar(Socket servidor, Object[] claves, PublicKey serverKey, String email) throws IOException, ClassNotFoundException {
        initComponents();
        this.servidor = servidor;
        this.claves = claves;
        this.serverKey = serverKey;
        this.email  = email;
        
        //Centra la ventana en el monitor
        setLocationRelativeTo(null);


        Image imgIcon = new ImageIcon (getClass().getResource("/Imagenes/ico.png")).getImage();
        setIconImage(imgIcon);
        
        
        
        DataOutputStream dos = new DataOutputStream(servidor.getOutputStream());
        dos.writeUTF(email);

        Usuario u = (Usuario) Utilidades.Util.recibirObjeto(servidor);

        txtNombre.setText(u.getNombre());
        txtApellidos.setText(u.getApellidos());

        if(u.getActivado() == 1){
            jcbActivado.setSelected(true);
        }else{
            jcbActivado.setSelected(false);
        }

        if(u.getTipoUser().equals("Admin")){
            jrbAdmin.setSelected(true);
        }else{
            jrbUser.setSelected(true);
        }
        
    }

