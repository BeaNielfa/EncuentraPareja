    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        try {
            // TODO add your handling code here:
            String nombre = (txtNombre.getText());
            String apellidos = (txtApellidos.getText());
            int activado;
            String tipo ="";
            if(jcbActivado.isSelected()){
                activado =(1);
            }else{
                activado = (0);
            }  
            if(jrbUser.isSelected()){
                tipo = ("User");
            }else{
                tipo = ("Admin");
            } 
            Usuario u = new Usuario (nombre,apellidos,email,activado,tipo);
            Utilidades.Util.enviarObject(servidor, u);
            
            
            this.setVisible(false);
            FrmAdmin f = new FrmAdmin(servidor, claves, serverKey);
            f.setVisible(true);
        } catch (IOException ex) {
            Logger.getLogger(FrmModificar.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrmModificar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

   

